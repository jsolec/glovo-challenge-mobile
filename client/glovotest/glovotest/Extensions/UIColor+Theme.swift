import UIKit

extension UIColor {
    static var redStroke: UIColor {
        return UIColor(red: 1.0, green: 0.0, blue: 0.0, alpha: 1.0)
    }
    static var redZone: UIColor {
        return UIColor(red: 1.0, green: 0.0, blue: 0.0, alpha: 0.2)
    }
}
